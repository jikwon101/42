#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	m;
	int n;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb >= 0)
	{
		m = nb / 10;
		n = nb % 10 + '0';
		if (m != 0)
			ft_putnbr(m);
		write(1, &n, 1);
	}
	else
	{
		nb = (-1) * nb;
		write(1, "-", 1);
		ft_putnbr(nb);
	}
}
