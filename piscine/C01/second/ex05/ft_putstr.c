#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while (str[count] != 0)
		count++;
	while (i < count)
	{
		write(1, &str[i], 1);
		i++;
	}
}
